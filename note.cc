#include <bits/stdc++.h>

class Note {
    short pitch;

    public:
        Note(short p) {
            pitch = p;
        }
};

/*
*   Guitars are tuned where the lowest note is E, which we are setting to pitch 0.
*/

Note E = Note(0);
Note F = Note(1);
Note Gb = Note(2);
Note G = Note(3);
Note Ab = Note(4);
Note A = Note(5);
Note Bb = Note(6);
Note B = Note(7);
Note C = Note(8);
Note Db = Note(9);
Note D = Note(10);
Note Eb = Note(11);

